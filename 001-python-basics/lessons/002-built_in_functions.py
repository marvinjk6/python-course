# print é built-in function
print("Built-in function")

# Número absoluto - distancia do número até o 0, independente se positivo ou negativo
print(abs(10.5))  # returns: 10.5
print(abs(-10.5))  # returns: 10.5

""" funções com mais de um argumento """
# pow(número, potência)
print(pow(2, 5))
print(pow(10, 3))
# min e max
print("Maior número: " + str(max(55, 88, 33)))
print("Menor número: " +str(min(55, 88, 33)))
